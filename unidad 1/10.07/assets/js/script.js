let num1 = 4;
let num2 = 3;

console.log("Primer número: " + num1);
console.log("Segundo número: " + num2);
console.log("");

// Operaciones matemáticas básicas
// Suma

let suma = num1 + num2;
console.log("Suma: " + suma);

// Resta

let resta = num1 - num2;
console.log("Resta: " + resta);

// Multiplicación
let multiplicacion = num1 * num2;
console.log("Multiplicación: " + multiplicacion);

// División
let division = num1 / num2;
console.log("División: " + division);

//Potencia
let potencia = num1 ** num2
console.log("Potenciación: " + potencia);

//Módulo
let modulo = num1 % num2
console.log("Módulo: " + modulo);

console.log("");

// Operadores de comparación
//Igualdad ==
//No le importa el tipo de dato
console.log(3 == 3);

//Identidad ===
//El tipo de dato debe ser el mismo
console.log(3 === '3');

//Desigualdad !=
console.log("3 != 4 = " + (3 != 4));

//Negación de la identidad !==
console.log("3 !== 3 = " + (3 !== 3));

//Mayor que
console.log("4 > 3 = " + (4 > 3));
console.log("3 > 4 = " + (3 > 3));

//Mayor igual que
console.log("4 >= 3 =" + (4 >= 3));
console.log("3 >= 3 =" + (3 >= 3));

//Menor que
console.log("4 < 3 = " + (4 > 3));
console.log("3 < 4 = " + (3 > 3));

//Menor igual que
console.log("4 <= 3 = " + (4 <= 3));
console.log("3 <= 3 = " + (3 <= 3));
